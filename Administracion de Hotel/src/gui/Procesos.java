    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        valorTot=Integer.parseInt(txtPreR.getText())*Integer.parseInt(txtDias.getText())*Integer.parseInt(txtPersonas.getText()); 
        if(txtSpa.getText().equals("si")){
            valorServicios=valorServicios+Float.parseFloat(txtpSpa.getText())*Float.parseFloat(txtDias.getText());
            System.out.println(valorServicios);
        }else{
            System.out.println("ERROR");
        }
        
        if(txtGim.getText().equals("si")){
            valorServicios=valorServicios+Float.parseFloat(txtpGim.getText())*Float.parseFloat(txtDias.getText());
            System.out.println(valorServicios);
        }else{
            System.out.println("ERROR");
        }
        
        if(txtBar.getText().equals("si")){
            valorServicios=valorServicios+Float.parseFloat(txtpBar.getText())*Float.parseFloat(txtDias.getText());
            System.out.println(valorServicios);
        }else{
            System.out.println("ERROR");
        }
        
        if(txtLimpieza.getText().equals("si")){
            valorServicios=valorServicios+Float.parseFloat(txtpLim.getText())*Float.parseFloat(txtDias.getText());
            System.out.println(valorServicios);
        }else{
            System.out.println("ERROR");
        }
        
        

        valFinal=valorTot+valorServicios;
        iva=0.12f*valFinal;
        Total=iva+valFinal;
        txtTotalF.setText(String.valueOf(Total));
        txtIvaF.setText(String.valueOf(iva));
        txtTotal.setText(String.valueOf(valFinal));
        txtSubtotalF.setText(String.valueOf(valFinal));
        txtCostosServiciosF.setText(String.valueOf(valorServicios));
        txtCostoEstanciaF.setText(String.valueOf(valorTot));
        
        valFinal=0;
        valorTot=0;
        valorServicios=0;
        
    }//GEN-LAST:event_btnCalcularActionPerformed

